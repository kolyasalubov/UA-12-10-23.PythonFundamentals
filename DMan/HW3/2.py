#  Якщо замовник бажає використовувати лише цілі значення, тоді ми маємо
#  використовувати тип int, тобто:
#  A = int(input("Please, enter the first number: ")) 



A = float(input("Please, enter the first number: "))
B = float(input("Please, enter your second number: "))


print("A + B = ", A + B)
print("A - B = ", A - B)
print("A * B = ", A * B)
print("A / B = ", A / B)
print("A**B = ", A**B)
print("A//B = ", A//B)
print("A%B = ", A%B) 
print("(A + B)**A =", (A + B)**A)
print("- A - B = ", - A - B)