# multiplication

number = input("Number with four digit ")
number_one = int(number[0])
number_two = int(number[1])
number_three = int(number[2])
number_four = int(number[3])
number = number_one * number_two * number_three * number_four
print(number)

# reverse order
number = input()[::-1]
print(number)

# sorted

print(sorted(number))